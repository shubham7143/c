//wap to understand operators...
#include<stdio.h>
int main(){
	//Bitwise operators.
	int a=10,b=20;
	//AND
	printf("%d\n",a&b);
	//OR
	printf("%d\n",a|b);
	//NOT
	printf("%d\n",~a);
	//XOR
	printf("%d\n",a^b);
	//Left Shift
	printf("%d\n",a<<b);
	//Right Shift
	printf("%d",a>>b);
	return 0;
}
