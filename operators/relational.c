//wap to understand operators...
#include<stdio.h>
int main(){
	//relational operators.
	int a=10,b=20;
	//less than
	printf("%d\n",a<b);
	//greater than
	printf("%d\n",a>b);
	//equal to
	printf("%d\n",a==b);
	//less than or equal to
	printf("%d\n",a<=b);
	//greater than or equal to
	printf("%d\n",a>=b);
	//not equal to
	printf("%d\n",a!=b);
	return 0;
}
